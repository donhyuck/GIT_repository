// 축소버전 풀이 2
abstract class 음식점 {
	String 업소명;
	
	void 주문(String 주문자명, String 주문자_번호, String 음식맵기, String 주문자_음식) {
		System.out.printf("%s에서 %s(%s)에게 %s %s(을)를 배달합니다.\n", 업소명, 주문자명, 주문자_번호, 음식맵기, 주문자_음식);
	}
}

class 영화반점 extends 음식점 {
	영화반점() {
		업소명 = "영화반점";
	}
}

class 북경반점 extends 음식점 {
	북경반점() {
		업소명 = "북경반점";
	}
}